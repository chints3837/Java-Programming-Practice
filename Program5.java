

class Program5
{
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("Before Swapping :");
		System.out.println("Value of a :"+a);
		System.out.println("Value of b :"+b);
		b=b-a;
		a=a+b;

		System.out.println("After Swapping :");
		System.out.println("Value of a :"+a);
		System.out.println("Value of b :"+b);

	}
}